// Arrow Function.


const sum = (a, b) => {
  console.log(a + b);
}
const sub = (a, b) => {
  console.log(a - b);
}
const mul = (a, b) => {
  console.log(a * b);
}
const div = (a, b) => {
  console.log(a / b);
}
const pow = (a, b) => {
  console.log(a ** b);
}

const cube = n => n * n * n

sum(5, 4);
sub(5, 4);
mul(5, 4);
div(5, 4);
pow(5, 4);
cube(5);